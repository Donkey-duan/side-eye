"use client";

import { monthLabel, spanLabel, type JudicialData } from "../data";

/**
 * 司法統計的方法說明。
 *
 * 這一區的數字不是機關直接發布的，是本站從裁判書全文判讀出來的，
 * 因此判準、分母與已知限制都必須攤開寫清楚，讀者才有辦法判斷該不該相信，
 * 也才有辦法指出哪裡算錯。
 */
export default function JudicialMethod({ data }: { data: JudicialData }) {
  const months = data.months;
  const coverage = months.length > 0
    ? `${monthLabel(months[0])} 至 ${monthLabel(months.at(-1) ?? "")}，共 ${spanLabel(months.length)}`
    : "尚未匯入";

  return (
    <div className="method-notes">
      <h3>司法統計的資料來源與計算方式</h3>
      <p className="method-lead">
        本站的裁判統計不是任何機關發布的數字，是從司法院公開的裁判書全文逐份判讀後彙整的。
        以下寫明每一步怎麼做、分母怎麼取、以及哪些是這套方法看不到的。
      </p>

      <dl>
        <dt>資料來源</dt>
        <dd>
          司法院資料開放平台的「裁判書」資料集，每月一包的裁判書全文，依政府資料開放授權條款第一版使用。
          目前涵蓋 {coverage}，收錄 {data.coverage.judgments.toLocaleString()} 筆性平相關刑事判決。
          新資料另以司法院裁判書 API 每晚累積，該 API 僅於臺灣時間 00:00 至 06:00 開放，且一次只提供單日異動清單。
        </dd>

        <dt>哪些判決會被收進來</dt>
        <dd>
          以「案由」比對性自主、性騷擾、性侵害、性剝削、妨害風化、跟蹤騷擾、猥褻、性影像、性交易九個詞，
          且必須是刑事判決。以案由而非全文比對，是因為用全文會把大量無關案件收進來——
          一件詐欺案只要內文引用了相關法條就會命中。
          裁定（羈押、補正、發還扣押物等程序事項）與附帶民事訴訟（審理的是損害賠償，不是刑責）都不計入。
        </dd>

        <dt>判決結果怎麼判定</dt>
        <dd>
          只讀主文，不讀理由。理由部分常引述其他案件與被告前科，用全文判斷會誤判。
          主文出現科刑用語即為有罪；同時出現科刑與無罪（主文寫「其餘被訴部分無罪」）歸為部分有罪；
          不受理、免訴、管轄錯誤移送各自成立一類；上訴審主文寫「上訴駁回」的歸為維持原判。
          每一筆都附原始裁判書連結，分類正不正確可以逐筆檢驗。
        </dd>

        <dt>為什麼有兩張表，分組還不一樣</dt>
        <dd>
          因為用錯分組會讓定罪率憑空變高。有罪判決的主文寫「犯強制猥褻罪」，
          無罪判決的主文只寫「被告無罪」，罪名根本不會出現。若依主文的罪名分組，
          無罪案件會因為分不到罪名而全部集中到「其他」，剩下每一類的定罪率都逼近百分之百。
          實測第一審 389 件無罪判決中，有 275 件會這樣被錯置。
          <br />
          因此凡是比較結果的統計，一律依<b>案由</b>分組——案由在起訴時就決定，
          與判決結果無關。代價是比較粗，妨害性自主底下不分強制性交或對未成年。
          刑度另外用一張表，依主文實際論的罪分組，分得比較細；那張表只涵蓋定罪案件，
          本來就不含無罪，所以沒有同樣的問題，但也因此不能拿來算定罪率。
        </dd>

        <dt>主文的罪名怎麼歸類</dt>
        <dd>
          一份判決只給一個標籤，取最先命中的類別：先分出性質完全不同的
          （判決確定後未履行治療或登記義務、媒介與圖利性交易、散布猥褻物品），
          再依被害人身分分（未滿十四歲、十四至十六歲、權勢或照護關係），
          最後才是行為態樣（強制性交、強制猥褻等）。因此一件「對未滿十四歲之人強制猥褻」
          會被歸在年齡那一類，而不是強制猥褻。
        </dd>

        <dt>每個比率的分母是誰</dt>
        <dd>
          站上的三個比率分母各自不同，不能互相加減，因此每個數字旁邊都附上分子與分母：
          <br />
          定罪率＝（有罪＋部分有罪）÷（有罪＋部分有罪＋無罪），分母是進入實體審理的件數。
          <br />
          未進入實體審理的比例＝（不受理＋免訴）÷ 全部件數，分母是該罪名的所有案件。
          <br />
          緩刑比例＝獲緩刑的定罪件數 ÷ 定罪件數，分母是定罪件數。
          <br />
          所以「定罪率 99%、不受理 17%」並不矛盾：後者說的是有 17% 的案件根本沒走到
          法院認定事實那一步，前者說的是在有走到的案件裡有 99% 被判有罪。
        </dd>

        <dt>為什麼定罪率只算第一審</dt>
        <dd>
          有兩個理由。一是避免重複計算：一個案件會歷經第一審、第二審甚至第三審，
          把各審級都放進同一個定罪率，同一件事實就會被算好幾次。二是要追出一個案件
          上訴到底、最後確定的結果，必須把它在各審級的裁判串起來，這在公開資料上很難
          可靠地做到；以第一審為準，每個案件才有一個一致、明確的計算基準。
          <br />
          要澄清的是，上訴審並非與有罪無罪無關。第一審與第二審都是事實審，都會認定事實、
          判斷犯罪是否成立，第二審可以推翻第一審的認定；第三審是法律審，原則上不重新認定
          事實。因此主文載明改判有罪或無罪的上訴審裁判，本站都會分類。無法從主文判斷的
          只有兩種：「上訴駁回」——可能是實體上維持原判，也可能是上訴不合程式（例如未繳費、
          未提上訴理由）而被程序駁回，後者確實與有罪無罪無關；以及「原判決撤銷、發回更審」，
          案件退回重審、尚未有實體結論。
          <br />
          所以第一審定罪率反映的是第一審的結果，不等於上訴確定後的最終結果——例如第一審
          判有罪，可能在第二審因核心證據被認定不可採而撤銷，發回重審後因證據不足改判無罪。
          性相關案件常卡在證據，這種翻轉並不罕見。各審級的結果另外分開呈現。
        </dd>

        <dt>「無罪」不等於「沒有發生」</dt>
        <dd>
          刑事審判以無罪推定為出發點：被告在被證明有罪之前，法律上先假定為無罪。要定罪，
          必須達到超越合理懷疑的高度確信，而且據以認定的每一項證據都必須合法、符合法定要式，
          只要其中一環達不到，就不能定罪。
          <br />
          因此判決寫「無罪」，和「事實上沒有發生」是兩回事。無罪可能來自：行為時欠缺罪責、
          有阻卻違法或阻卻罪責的正當理由，或最常見的——證據不足以達到定罪所需的確信門檻。
          性侵害與性騷擾多發生在只有兩人、缺乏目擊者的場合，最常卡在證據，因此「因證據不足
          而無罪」本來就佔相當比例。這不代表被害人說謊，也不代表傷害不存在。
        </dd>

        <dt>刑期與緩刑</dt>
        <dd>
          取主文宣告的自由刑，數罪併罰時以「應執行刑」為準，那才是實際要服的刑期。
          以中位數與四分位距呈現而非平均值，因為少數重刑會把平均值整個拉走。
          無期徒刑與死刑不換算成月份，另計件數。緩刑比例的分母是定罪件數。
        </dd>

        <dt>這套方法看不到什麼</dt>
        <dd>
          未上網公開的裁判不在其中，因此本站的件數少於實際的案件數。
          「上訴駁回」的主文不會重述原審判的是有罪還是無罪，維持的是哪一種結果，本站不推測；
          它也可能是上訴不合程式的程序駁回，與有罪無罪無關。
          判決書中以代號或○標示的法官姓名無法辨識，不列入法官統計；
          合議庭一份判決有多位法官，因此各法官件數的加總會大於判決總數。
          所有分類都是由程式判讀主文得出的，會有錯，發現錯誤請循更正頁面告知。
        </dd>

        <dt>這些數字不能拿來做什麼</dt>
        <dd>
          統計呈現的是歷來的裁判樣態，不是對個案結果的預測，也不代表任何法官的立場或能力。
          個案的事實、證據與量刑因素差異極大，是非仍應回到判決本身。
        </dd>
      </dl>
    </div>
  );
}
